// 패키지 선언: 'main' 패키지를 정의한다.
// Go 프로그램은 'main' 패키지에서 시작된다.
package main

// 'fmt' 패키지를 가져온다.
// 'fmt'는 출력을 처리하는 표준 라이브러리이다.
import "fmt"

// 'main' 함수: 프로그램의 시작점이다.
// 이 함수는 프로그램이 실행될 때 호출된다.
func main() {
    // 'fmt.Println' 함수로 문자열을 화면에 출력한다.
    // 여기서는 "Hello Go"가 출력된다.
    fmt.Println("Hello Go")
}
